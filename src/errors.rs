#[derive(Debug)]

pub enum AppError {
    Io(std::io::Error),
    Json(serde_json::Error),
    InvalidInput(String),
}

impl From<std::io::Error> for AppError {
    fn from(e: std::io::Error) -> Self {
        AppError::Io(e)
    }
}

impl From<serde_json::Error> for AppError {
    fn from(e: serde_json::Error) -> Self {
        AppError::Json(e)
    }
}
